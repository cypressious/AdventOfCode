fun main(args: Array<String>) {
    var state = input
    //    var state = """##.#.#
    //...##.
    //#....#
    //..#...
    //#.#..#
    //####.#""".splitLines()

    val maxValue = state.size - 1
    val corners = intArrayOf(0, maxValue)
    val conditionForOn = intArrayOf(2, 3)

    state = state.mapIndexed { i, s ->
        if (i in corners) {
            '#' + s.drop(1).dropLast(1) + '#'
        } else {
            s
        }
    }

    repeat(100) {
        state = (0..maxValue).map { x ->
            buildString {
                for (y in 0..maxValue) {
                    append(if (x in corners && y in corners) {
                        '#'
                    } else if (state.isOn(x, y)) {
                        if (state.neighborsOn(x, y) in conditionForOn) {
                            '#'
                        } else {
                            '.'
                        }
                    } else {
                        if (state.neighborsOn(x, y) == 3) {
                            '#'
                        } else {
                            '.'
                        }
                    })
                }
            }
        }

        state.joinToString(separator = "\n").print()
        println()
    }

    state.map { it.count { it == '#' } }.sum().print()
}

private fun List<String>.neighborsOn(x: Int, y: Int): Int {
    var count = 0

    for (xx in -1..1) {
        for (yy in -1..1) {
            if (xx == 0 && yy == 0) {
                continue
            }

            if (isOn(x + xx, y + yy)) {
                count++
            }
        }
    }

    return count
}

private fun List<String>.isOn(x: Int, y: Int): Boolean {
    if (x < 0 || x >= size) {
        return false
    }

    val column = get(x)
    if (y < 0 || y >= column.length) {
        return false
    }

    return column[y] == '#'
}

